# 산술 연산 프로그램 작성
na = 10
nb = 20.2
result = na - nb
print( na , "-" , nb , "=" , result )
print( "na 값" , na , "빼기" , "nb 값" , nb , "의 결과값은" , result , "다.")
result = na * nb
print( na , "*" , nb , "=" , result )
print( "na 값" , na , "곱하기" , "nb 값" , nb , "의 결과값은" , result , "다.")
result = na / nb
print( na , "/" , nb , "=" , result )
print( "na 값" , na , "나누기" , "nb 값" , nb , "의 결과값은" , result , "다.")