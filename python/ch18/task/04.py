import turtle as t

t.title("정다각형 그리기")
t.setup(700,700)

ct = t.Turtle()
ct.shape("turtle")
ct.pencolor("skyblue")
ct.pensize(3)

ct.color("black","gold")
ct.begin_fill()

for i in range(4):
    ct.right(90)
    ct.fd(100)

ct.forward(100)
ct.left(90)

for i in range(5):
    ct.right(360/5)
    ct.fd(100)

ct.forward(100)
ct.left(90)

for i in range(8):
    ct.right(360/8)
    ct.fd(100)

ct.end_fill()


t.exitonclick()

