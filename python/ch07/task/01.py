# 리턴 값 설정

def funca(pa,pb):
    temp = pa
    pa = pb
    pb = temp

na = 10
nb = 11
print("na 값은",na,"nb값은",nb)
funca(na,nb)
print("na 값은",na,"nb값은",nb)

def funcb(pa,pb):
    temp = pa
    pa = pb
    pb = temp
    return(pa,pb)

na = 10
nb = 11
print("na 값은",na,"nb값은",nb)
n = funcb(na,nb)
print("na 값은",n[0],"nb값은",n[1])