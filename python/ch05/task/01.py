# for문 연습

characters = [ '앨리스' , '도도새' , '3월토끼' ]
for character in characters :
    print(character)

for letter in '체셔고양이' :
    print(letter)

nums = [ 0 , 1 , 2 ]
for num in nums :
    print(num)
    print(nums)

# range 함수 연습

print(list(range(10)))
print(list(range(3,10)))
print(list(range(8,14,2)))

for num in range(3) :
    print( ' 안녕 거북이' , num )

